

# ¿Qué son las Variables?

# Python ayudará con ello. Python ofrece "cajas" (contenedores) 
# especiales para este propósito, estas cajas son llamadas variables - el nombre 
# mismo sugiere que el contenido de estos contenedores puede variar en casi 
# cualquier forma.

# ¿Cuáles son los componentes o elementos de una variable en Python?

# Un nombre.
# Un valor (el contenido del contenedor).


# Si se desea nombrar una variable, se deben seguir las siguientes reglas:

# El nombre de la variable debe de estar compuesto por MAYÚSCULAS, minúsculas, dígitos, y el carácter _ (guion bajo).
# El nombre de la variable debe comenzar con una letra.
# El carácter guion bajo es considerado una letra.
# Las mayúsculas y minúsculas se tratan de forma distinta (un poco diferente que en el mundo real - Alicia y ALICIA son el mismo nombre, 
# pero en Python son dos nombres de variable distintos, subsecuentemente, son dos variables diferentes).
# El nombre de las variables no pueden ser igual a alguna de las palabras reservadas de Python (se explicará más de esto pronto).



# También es posible usar letras mixtas (por ejemplo: miVariable), 
# pero solo en contextos donde ese ya es el estilo predominante, para mantener 
# la compatibilidad retroactiva con la convención adoptada.

